import mongoose from 'mongoose'
import * as types from '../types'

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  created: {
    type: Date,
    default: Date.now,
  },
})

const User = mongoose.model<types.User>('User', userSchema)

export default User
